
mood = input("How you feel today? ")

if mood == "happy":
    print("It is great to see you happy!")
elif mood == "nervous":
    print("Take a deep breath 3 times.")
elif mood == "sad":
    print("Don't worry, be happy! :)")
elif mood == "excited":
    print("WOW, great!")
elif mood == "relaxed":
    print("Just keep going like that.")
else:
    print("I don't recognize this mood!")
